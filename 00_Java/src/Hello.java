
public class Hello {

	public static void main(String[] args) {
		//ln 은 문장이 끝나고 나서 엔터 처리
		System.out.print("안녕하세요.");
		System.out.println("반갑습니다.");
		System.out.println("환영합니다.");
		System.out.println("어서오세요. 점심시간");
		System.out.print("테스트");
		//들여쓰기는 Tab 이용
		System.out.println("1");
		System.out.println("235464");
		System.out.println("01234");
		System.out.println(1);
		System.out.println(01);
		System.out.println(true); // true = 참
		//단축키 sysout 작성후 ctrl키와 space키 누르면 자동으로 system.out.println()
		System.out.println("예제");
		System.out.println(false);
		//0을 그냥 앞에 넣고 01을 쓰면 단순히 1로 출력이 됨
		//앞에 0을 출력하고 싶으면 "" 큰 따옴표를 이용해서 출력
	}

}