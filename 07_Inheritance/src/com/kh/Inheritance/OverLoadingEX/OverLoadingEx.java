package com.kh.Inheritance.OverLoadingEX;

public class OverLoadingEx {
/*
 OverLoading이란?
  같은 method 이름을 가진 method 를 여러개 정의하는 것
  매개변수의 갯수나 타입이 다를 때 사용한다.
  
 Over         : ~ 위에
 Loading      : 기록되어 있는 것을 호출하는 것
 Over Loading : 똑같은 이름의 메서드가, 다양한 형태로 넘쳐 흐른다는 의미로 사용한다.
 
 예제 코드
  // 메서드 - 생성자 - 기본
	  public Person() {
	  
	  }
  
  // 메서드 - 생성자 - 필수
	  public Person(String name) {
	  	this.name = name;
	  }
  
  // 메서드 - void(아무 것도 없음) - 출력
	  public void walk() {
	  	System.out.println("걷습니다.");
	  }
  
	  public void walk(String 사람) {
	  	System.out.println(사람 + " 이 걷습니다.");
	  }
 */
}
